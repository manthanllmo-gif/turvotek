document.addEventListener('DOMContentLoaded', function() {
    var goToTopBtn = document.getElementById('goToTopBtn');
    
    if (goToTopBtn) {
        window.addEventListener('scroll', function() {
            if (window.scrollY > 400) {
                goToTopBtn.classList.add('visible');
            } else {
                goToTopBtn.classList.remove('visible');
            }
        });
        
        goToTopBtn.addEventListener('click', function() {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });
    }

    // Rotating Text & Image Animation
    var rotatingElements = document.querySelectorAll('.rotating-text');
    rotatingElements.forEach(function(el) {
        var words, images;
        try {
            words = JSON.parse(el.getAttribute('data-words'));
            images = JSON.parse(el.getAttribute('data-images'));
        } catch(e) { return; }
        if (!words || words.length < 2) return;
        var index = 0;
        var currentText = el.textContent.trim();
        var startIndex = words.indexOf(currentText);
        if (startIndex !== -1) index = startIndex;
        var imageId = el.getAttribute('data-image-id') || 'rotatingShowcaseImage';
        var targetImg = document.getElementById(imageId);

        function rotateWord() {
            index = (index + 1) % words.length;
            el.style.opacity = '0';
            el.style.transform = 'translateY(6px)';
            el.style.transition = 'opacity 0.3s ease, transform 0.3s ease';
            if (targetImg) {
                targetImg.style.opacity = '0';
                targetImg.style.transform = 'scale(0.95)';
                targetImg.style.transition = 'opacity 0.3s ease, transform 0.3s ease';
            }
            setTimeout(function() {
                el.textContent = words[index];
                el.style.opacity = '1';
                el.style.transform = 'translateY(0)';
                if (targetImg && images && images[index]) {
                    targetImg.src = images[index];
                    targetImg.style.opacity = '1';
                    targetImg.style.transform = 'scale(1)';
                }
            }, 300);
        }

        el.addEventListener('transitionend', function() {
            el.style.transition = '';
            el.style.transform = '';
        });

        setInterval(rotateWord, 3500);
    });
});
</script>

